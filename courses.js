const modal = document.getElementById("courseModal");
const modalBody = document.getElementById("modal-body");
const closeBtn = document.querySelector(".close");

// Open modal when button clicked
document.querySelectorAll(".course-card .view-btn").forEach(btn => {
  btn.addEventListener("click", (e) => {
    const course = e.target.closest(".course-card").dataset.course;
    modalBody.innerHTML = courseDetails[course];
    modal.style.display = "flex";
  });
});

// Close modal
closeBtn.addEventListener("click", () => {
  modal.style.display = "none";
});

// Close when clicking outside modal box
window.addEventListener("click", (e) => {
  if (e.target === modal) {
    modal.style.display = "none";
  }
});


document.addEventListener("click", function (e) {
  const btn = e.target.closest(".register-btn");
  if (!btn) return;

  const course = btn.dataset.course;

  
  window.location.href = `registration.html?course=${course}`;
});
